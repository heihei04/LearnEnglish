<div class="lesson-block success">
  <div class="lesson-block-header">⭐ Jake's 핵심 정리 (Summary)</div>
  <p>✅ <strong>형용사</strong>: 명사를 꾸밈 (a <em>kind</em> person)</p>
  <p>✅ <strong>부사</strong>: 동사·형용사·부사를 꾸밈 (she speaks <em>kindly</em>)</p>
  <p>✅ <strong>형용사 → 부사</strong>: 보통 -ly 추가, 하지만 good→well, fast→fast 같은 예외 있음</p>
  <p>✅ <strong>빈도부사</strong>: 일반동사 앞, be동사 뒤, 조동사+동사 사이</p>
  <p>✅ <strong>비교급</strong>: 짧은 단어 + -er than, 긴 단어 more + ~ than</p>
  <p>✅ <strong>최상급</strong>: 짧은 단어 the + -est, 긴 단어 the most + ~</p>
  <p>✅ <strong>불규칙</strong>: good → better → the best / bad → worse → the worst</p>
</div>

